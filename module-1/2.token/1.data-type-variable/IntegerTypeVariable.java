class IntegerTypeVariable{
	public static void main(String[] args){
		byte byteTypeVariable;
		byteTypeVariable=1;
		System.out.println(byteTypeVariable);

		short shortTypeVariable;
		shortTypeVariable=12;
		System.out.println(shortTypeVariable);

		int intTypeVariable;
		intTypeVariable=122345;
		System.out.println(intTypeVariable);

		long longTypeVariable;
		longTypeVariable= 9973914185L;
		System.out.println(longTypeVariable);




	}
}