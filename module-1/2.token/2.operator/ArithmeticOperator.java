class ArithmeticOperator{
	public static void main(String[] args){
		int i=10;
		System.out.println("Content Of i: " + i);

		int j=5;
		System.out.println("Content Of j: " + j);

		int a=i+j;
		System.out.println("Addition of "+i +" and "+j +" is "+ a);

		int b=i-j;
		System.out.println("difference of "+i +" and "+j +" is "+ b);

		int c= i+j;
		System.out.println("Multiplication of "+i +" and "+j +" is "+ c);


		int d= i/j;
		System.out.println("Division of "+i +" and "+j +" is "+ d);

		int e=i%j;
		System.out.println("Modulus of "+i +" and "+j +" is "+ e);

	}
}