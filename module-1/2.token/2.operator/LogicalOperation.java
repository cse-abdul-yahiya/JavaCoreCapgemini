class LogicalOperation{
	public static void main(String[] args){
		int  i = 10;
		System.out.println("Content of i is : " + i );

		int j = 30;
		System.out.println("Content of j is : " + j );

		boolean x = i>j;
		System.out.println("Content of x is : " + x );

		boolean y = i<j;
		System.out.println("Content of y is : " + y );

		boolean and = x && y;
		System.out.println("Content of And is : " + and );


		boolean or = x || y;
		System.out.println("Content of or is : " + or );






	}
}